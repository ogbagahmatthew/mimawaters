<p class="__abomi ">Disinfection / Dosage</p>


<p class="__abomi1 ">All tanks, pipes etc. , which lead water, germinate with time and it forms the so-called biofilm in which microbes live and contaminate the stored water there.

Therefore, all water-bearing containers must be cleaned regularly - the so-called basic or disinfection must be performed.

It should be done at least once or twice a year, or if water contamination is suspected or biofilm is suspected (usually slimy deposits on the surfaces).

The dosage of chlorine dioxide solution is calculated from the volumes in liters of the water-carrying systems and the recommended dosing instructions ml of chlorine dioxide solution / liter of water.

The dosage in the disinfection is significantly higher than the permitted concentration of chlorine dioxide in drinking water, it is based on 5mg chlorine dioxide / liter /, in case of heavy infestation with biofilm is a higher concentration to choose -ca.10-20 mg chlorine dioxide / liter of water.

The water used for disinfecting the stand must be discarded or used for washing the clothes, for cleaning or for toilet flushing.

The finished chlorine dioxide solution should be carefully pouredin a half full tank and then fill the tank completely with water. If possible, all parts should be touched with water to achieve complete cleaning. All taps are to be opened briefly so that they too are flushed through. After approx. 3 hours exposure, open all taps once again and let drain some water.

After 12 - 24 h exposure time, the water can be removed, the biofilm is dead and gradually falls off, germs are destroyed.

Now fresh water can be filled, which is to be treated according to the regulations for the treatment of drinking water with chlorine dioxide solution.

See MIMA dosing instructions!

With each new filling of the containers / tanks with new fresh water / drinking water, ready-to-use chlorine dioxide solution must be added (see dosing instructions MIMA Water Cleanser) in order to maintain the basic disinfection of the water.
</p>



