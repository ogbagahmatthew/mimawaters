7.	Disinfection of drinking water 10 ml / 50 liters of water<br>

The recommended use of chlorine dioxide solutions may be subject to fluctuations, depending on the degree of pollution and dissolved constituents of the raw water, as well as temperature and solar radiation contribute to the reduction of reactive chlorine dioxide.

When used in drinking water, after the chlorine dioxide has reacted, the limit value of 0.2 mg chlorine dioxide / liter water should not be exceeded or the recommended lower value of 0.05 mg ChlO2 / liter water should not be exceeded.
